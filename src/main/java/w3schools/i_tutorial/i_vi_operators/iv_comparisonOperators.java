package w3schools.i_tutorial.i_vi_operators;

public class iv_comparisonOperators {

    public static void main(String[] args) {

        //Java Comparison Operators
        /*
        Comparison operators are used to compare two values:
         */


        int a = 5;
        int b = 3;
        System.out.println("5 == 3 -> " + (a == b)); // returns false because 5 is not equal to 3

        int c = 5;
        int d = 3;
        System.out.println("5 != 3 -> " + (c != d)); // returns true because 5 is not equal to 3

        int e = 5;
        int f = 3;
        System.out.println("5 > 3 -> " + (e > f)); // returns true because 5 is greater than 3

        int g = 5;
        int h = 3;
        System.out.println("5 < 3 -> " + (g < h)); // returns false because 5 is not less than 3

        int i = 5;
        int j = 3;
        System.out.println("5 >= 3 -> " + (i >= j)); // returns true because 5 is greater, or equal, to 3

        int k = 5;
        int l = 3;
        System.out.println("5 <= 3 -> " + (k <= l)); // returns false because 5 is neither less than or equal to 3





    }
}
